void drawMap()
{
    for (int i = 0; i < 16; i++)
    {
        for (int j = 0; j < 16; j++)
        {
            if (map[i][j])
            {
                switch (map[i][j])
                {
                case 1:
                    printf(" # ");
                    break;
                case 2:
                    printf(" $ ");
                    break;
                case 3:
                    printf(" @ ");
                    break;
                default:
                    break;
                }
            }
            else
            {
                printf("   ");
            }
        }
        printf("\n");
    }
    printf("\n");
}

